export function translateTeam(name: string): string {
  if (!name) return name;
  const direct = TEAM_MAP[name];
  if (direct) return direct;

  const simplified = name
    .replace(/\s+(FC|AFC|CF|SC|BC|SCO)$/i, "")
    .replace(/\s+Calcio$/i, "")
    .trim();

  return TEAM_MAP[simplified] ?? name;
}

export function isSupportedLeague(id?: number | null, name?: string | null): boolean {
  if (typeof id === "number" && SUPPORTED_LEAGUE_IDS.has(id)) return true;
  if (!name) return false;

  const leagueName = name.includes(" · ") ? name.slice(0, name.indexOf(" · ")).trim() : name.trim();
  return SUPPORTED_LEAGUE_NAMES.has(leagueName);
}

export function translateLeague(raw: string): string {
  if (!raw) return raw;

  const separator = raw.includes(" · ") ? " · " : raw.includes("路") ? "路" : "";
  const leagueEn = separator ? raw.slice(0, raw.indexOf(separator)).trim() : raw.trim();
  const roundEn = separator ? raw.slice(raw.indexOf(separator) + separator.length).trim() : "";
  const leagueCn = LEAGUE_MAP[leagueEn] ?? leagueEn;

  if (!roundEn) return leagueCn;

  const matchdayMatch = roundEn.match(/^Matchday\s+(\d+)$/i);
  if (matchdayMatch) return `${leagueCn} · 第 ${matchdayMatch[1]} 轮`;

  let roundCn = roundEn;
  for (const [en, cn] of Object.entries(ROUND_MAP)) {
    if (roundEn.startsWith(en)) {
      roundCn = roundEn.replace(en, cn);
      break;
    }
  }

  return `${leagueCn} · ${roundCn}`;
}
